public class LinearDistanceFunction {
    public double s, e, tlen, alen, slen;

    public LinearDistanceFunction(double s, double e, double tlen, double alen, double slen) {
        this.s = s;
        this.e = e;
        this.tlen = tlen;
        this.alen = alen;
        this.slen = slen;
    }

}
